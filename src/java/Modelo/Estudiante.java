    String apell;
    String num_doc;
    String fech_nac;
    String ciud_nac;
    String barr_res;
    String direc_res;
    String edad;
    String genero;
    String rh;
    String eps;
    String telefono;
    String correo;
    String nomb_acud;
    String apell_acu;
    String usuario;
    String contras;
    int id_curso;

